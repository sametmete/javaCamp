
public class User {

	public User() {
		
	}
	
	
	public User(int Id, String firstName, String lastName, String email, String password) {
		super();
		this.Id = Id;
		this.firstName = firstName;
		this.lastName = lastName;
		Email = email;
		this.password = password;
		
	}
	
	int Id;
	String firstName;
	String lastName;
	String Email;
	String password;
	
}
